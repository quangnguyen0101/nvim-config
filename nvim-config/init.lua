-- pull lazy vim repo
print("Loading lazy vim")
local lazypath = vim.fn.stdpath("data") .. "/lazy/lazy.nvim"
if not vim.uv.fs_stat(lazypath) then -- why fs_stat undefined field?
    vim.fn.system({
        "git",
        "clone",
        "--filter=blob:none",
        "https://github.com/folke/lazy.nvim.git",
        "--branch=stable",
        lazypath,
    })
end
print("Lazy vim loaded")
vim.opt.rtp:prepend(lazypath)

